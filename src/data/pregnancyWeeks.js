// Get week data based on current week and language
export const getWeekData = (week, language = 'en') => {
  const weeks = pregnancyWeeks[language];
  // Find closest available week
  const availableWeeks = Object.keys(weeks).map(Number).sort((a, b) => a - b);
  const closestWeek = availableWeeks.find(w => w >= week) || availableWeeks[availableWeeks.length - 1];
  return weeks[closestWeek] || weeks[availableWeeks[0]];
};

